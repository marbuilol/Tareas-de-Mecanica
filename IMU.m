%% Cálculo de los ángulos y rotaciones de Euler y wx1, wy1, wz1

N=length(t); %longitud del vector t que es igual al de wx wy y wz
deltaT=zeros(1,N-1); %creación del vector ceros deltaT de dimensión 1x1000

phi0=0.1;   %ángulo phi inicial
theta0=0.2; %ángulo theta inicial
psi0=0.3;   %ángulo psi inicial

for i=1:1:N-1               %bucle para determinar los saltos de tiempo 
                            %sucesivos
    deltaT(i)=t(i+1)-t(i);
end

phi=zeros(1,N); %cración del vector ceros phi de dimensión 1x1001
phi(1)=phi0;    %siendo dato phi inicial lo sustituimos en la primera 
                %posición del vector ceros

theta=zeros(1,N); %Ídem
theta(1)=theta0;

psi=zeros(1,N);   %Ídem
psi(1)=psi0;

ROTEuler=zeros(3,N); %creación de una matriz que tendrá las rotaciones de
                     %Euler, siendo su dimensión 3x1001. Correspondiendo
                     %a la primera fila a la rot. de nutación, luego la 
                     %segunda fila a la rot. de precesión y la última fila
                     %a la rot. propia
                     
                     %Luego cada columna corresponde a un instante t.
                     %Siendo la primera columna t1, luego t2, t3,...,tN

W=zeros(3,N);        %Creación del vector W de dimensión 3x1001, cuyas 
                     %filas corresponden a wx, wy y wz respectivamente.
                     %luego cada columna hace referencia a un instante t.
                     %Siendo la primera columna t1, luego t2, t3,...,tN 
                     
W(1,:)=wx;           %Siendo dato wx, lo introducimos a la matriz W en su
                     %primera fila
                     
W(2,:)=wy;           %Siendo dato wy, lo introducimos a la matriz W en su
                     %segunda fila
                     
W(3,:)=wz;           %Siendo dato wz, lo introducimos a la matriz W en su
                     %tercera fila

wx1=zeros(1,N);      %Creación del vector wx1 correspondiente a las
                     %velocidades angulares en base fija.
wy1=zeros(1,N);      %Ídem
wz1=zeros(1,N);      %Ídem

format long          %Declaración de format long para conservar la mayor
                     %cantidad de decimales y minimizar el arrastre de
                     %error
syms a b             %Declaración de variables simbólicas para luego 
                     %sustituir en la matriz M
                     
for i=1:1:N    %Creación de un bucle que calcula primero los valores
               %de la matriz M en t(i), luego calcula la columna t(i) de 
               %la matriz de la rotaciones de Euler, luego calcula
               %los ángulos de Euler t(i+1) y finalmente calcula
               %las velocidades angulares de la base fija en t(i).
               %Y así sucesivamente hasta completar el bucle
                     
    M=[sin(a).*sin(b) cos(b) 0;     %Declaración de la matriz M que
   sin(a).*cos(b) -sin(b) 0;        %aparece en el paso de la base de
   cos(a) 0 1];                     %Euler a la base móvil. Y está dentro
                                    %del bucle porque se necesita volver
                                    %a definir las variables a y b para 
                                    %no conservar los valores del bucle
                                    %anterior
                                 
                                 
                                  
    M=eval(subs(M,[a,b],[theta(i),psi(i)])); %Sustitución de las variables
                                             %a y b por su correspondiente
                                             %valor en el bucle
                                             
    ROTEuler(:,i)=M\W(:,i); %Cálculo de la primera columna de la matriz que
                            %contiene las rotaciones de Euler mediante
                            %el paso de la base de Euler a la base móvil
                           
        %Cálculo de los ángulos de Euler
        if i<N               
        phi(i+1)=phi(i)+ROTEuler(1,i).*deltaT(i);
        theta(i+1)=theta(i)+ROTEuler(2,i).*deltaT(i);
        psi(i+1)=psi(i)+ROTEuler(3,i).*deltaT(i);
        end
        
    %Cálculo de las velocidades angulares de la base fija
    wx1(i)=ROTEuler(3,i).*sin(theta(i)).*sin(phi(i))+ROTEuler(2,i).*cos(phi(i));
    wy1(i)=-ROTEuler(3,i).*sin(theta(i)).*cos(phi(i))+ROTEuler(2,i).*sin(phi(i));   
    wz1(i)=ROTEuler(3,i).*cos(theta(i))+ROTEuler(1,i);
 
end
